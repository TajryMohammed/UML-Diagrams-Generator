package org.mql.java.models;

public class ModConstant {

    private String name;

    public ModConstant() {
        // Default constructor
    }

    public ModConstant(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
