package org.mql.java.models;



public class ModConstant {

    // Attributes :

    private String name;


    // Constructors :

    public ModConstant(){}

    public ModConstant(String name){
        this.name = name;
    }

    // Getters & Setters :


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

